###################################################
# Numeric variables
###################################################

# Integer - positive or negative whole number like -1, 1, 99, 9999999
my_int_variable = 1
print(my_int_variable)

# Add 20 to the number
my_int_variable = my_int_variable + 20
print(my_int_variable)

# Subract 1 from the number
my_int_variable = my_int_variable -1

# Divide the new number by 3 but only print the result
print(my_int_variable/4)

# Multiply the number by 2
print(my_int_variable * 2)